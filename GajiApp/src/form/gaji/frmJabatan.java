    private void btSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSimpanActionPerformed
        // TODO add your handling code here:
        if(evt.getActionCommand().equalsIgnoreCase("Tambah")){
            siapSimpan();
            kosongJText();
            txNmJab.requestFocus();
            isEdit = false;
        } else{
            if(txNmJab.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Nama Jabatan harus diisi.!", "Peringatan", JOptionPane.OK_OPTION);
                txNmJab.requestFocus();
                return;
            }
            if(txGapok.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Besar Gaji Pokok harus diisi.!", "Peringatan", JOptionPane.OK_OPTION);
                txGapok.requestFocus();
                return;
            } else {
                try {
                    double gp = Double.parseDouble(txGapok.getText());
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Besar Gaji Pokok harus diisi Angka!","Peringatan",JOptionPane.OK_OPTION);
                    txGapok.requestFocus();
                    return;
                }
            }
            
            Jabatan jbt = new Jabatan();            
            jbt.setNmJab(txNmJab.getText());
            jbt.setGaPok(Double.parseDouble(txGapok.getText()));
            
            SqlJabatan qjb = new SqlJabatan(KonekDB.getKonek());
            if(isEdit){
                jbt.setIdJab(Integer.parseInt(txId.getText()));
                qjb.update(jbt, Integer.parseInt(txId.getText()));
            }else{
                qjb.insert(jbt);
            }

            JOptionPane.showMessageDialog(this, "Data berhasil disimpan.");
            tampilData();            
            statusAwal();
        }
    }//GEN-LAST:event_btSimpanActionPerformed

