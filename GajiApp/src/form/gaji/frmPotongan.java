    private void btSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSimpanActionPerformed
        // TODO add your handling code here:
        if(evt.getActionCommand().equalsIgnoreCase("Tambah")){
            siapSimpan();
            kosongJText();
            txNmPot.requestFocus();
            isEdit = false;
        } else{
            if(txNmPot.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Nama Potongan harus diisi.!", "Peringatan", JOptionPane.OK_OPTION);
                txNmPot.requestFocus();
                return;
            }

            Potongan ptg = new Potongan();            
            ptg.setNmPot(txNmPot.getText());

            SqlPotongan qpt = new SqlPotongan(KonekDB.getKonek());
            if(isEdit){
                ptg.setIdPot(Integer.parseInt(txId.getText()));
                qpt.update(ptg, Integer.parseInt(txId.getText()));
            }else{
                qpt.insert(ptg);
            }

            JOptionPane.showMessageDialog(this, "Data berhasil disimpan.");
            tampilData();
            statusAwal();
        }
    }//GEN-LAST:event_btSimpanActionPerformed

    private void btEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditActionPerformed
        // TODO add your handling code here:
            isEdit = true;
            siapSimpan();
    }//GEN-LAST:event_btEditActionPerformed

    private void btHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btHapusActionPerformed
        // TODO add your handling code here:
            if (tblPotong.getSelectedRow() >= 0 && JOptionPane.showConfirmDialog(this, "Data akan dihapus?", "Konfirmasi", JOptionPane.OK_CANCEL_OPTION) == 0){
                SqlPotongan qpt = new SqlPotongan(KonekDB.getKonek());
                qpt.delete(Integer.parseInt(txId.getText()));
                
                tampilData();
                JOptionPane.showMessageDialog(this, "Data telah dihapus.");                
                statusAwal();
            }
    }//GEN-LAST:event_btHapusActionPerformed

