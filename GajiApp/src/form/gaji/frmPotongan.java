    private void statusAwal(){
        kosongJText();
        setInputan(false);
        btSimpan.setText("Tambah");
        btEdit.setText("Edit");
        btHapus.setText("Hapus");
        btTutup.setText("Tutup");
        btSimpan.setEnabled(true);
        btEdit.setEnabled(false);
        btHapus.setEnabled(false);
        lbId.setVisible(false);
        txId.setVisible(false);
    }
    
    private void siapSimpan(){
        setInputan(true);
        btSimpan.setText("Simpan");
        btTutup.setText("Batal");
        btSimpan.setEnabled(true);
        btEdit.setEnabled(false);
        btHapus.setEnabled(false);
    }

    private void tabelKlik(){
        setInputan(false);
        btSimpan.setEnabled(false);
        btEdit.setEnabled(true);
        btHapus.setEnabled(true);
        btTutup.setText("Batal");
    }

    private void kosongJText(){
        txId.setText("");
        txNmPot.setText("");
    }    

    private void setInputan(boolean aktif){
        txNmPot.setEnabled(aktif);
    }

    private void tampilData(){
        SqlPotongan qpt = new SqlPotongan(KonekDB.getKonek());
        tblPotong.setModel(new TblPotongan(qpt.getAll()));
        AutoFitTableColumns.autoResizeTable(tblPotong, true);
    }
