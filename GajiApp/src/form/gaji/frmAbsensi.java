    private void btSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSimpanActionPerformed
        // TODO add your handling code here:
        if(evt.getActionCommand().equalsIgnoreCase("Tambah")){
            siapSimpan();
            kosongJText();
            btCrPeg.requestFocus();
            isEdit = false;
        }else{
            if(txIdPeg.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Pegawai harus dipilih!", "Peringatan", JOptionPane.OK_OPTION);
                btCrPeg.requestFocus();
                return;
            }
            if(tx25Hr.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Jumlah Hari Kerja harus diisi!", "Peringatan", JOptionPane.OK_OPTION);
                tx25Hr.requestFocus();
                return;
            } else {
                try {
                    int t25kr = Integer.parseInt(tx25Hr.getText());
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this,"Jumlah Hari Kerja harus diisi Angka!","Peringatan",JOptionPane.OK_OPTION);
                    tx25Hr.requestFocus();
                    return;
                }
            }
            if(tx6Hr.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Jumlah Hari Hadir harus diisi!", "Peringatan", JOptionPane.OK_OPTION);
                btHtg.requestFocus();
                return;
            }
            
            Absensi abs = new Absensi();
            abs.setIdPeg(Integer.parseInt(txIdPeg.getText()));
            abs.setJmHrKj(Integer.parseInt(tx25Hr.getText()));
            abs.setjKrHr(Integer.parseInt(tx8Jam.getText()));
            abs.setjHrHd(Integer.parseInt(tx6Hr.getText()));
            abs.setBln((String) cbbBln.getSelectedItem());
            abs.setThn(Integer.parseInt(cbbThn.getSelectedItem().toString()));

            SqlAbsensi qab = new SqlAbsensi(KonekDB.getKonek());
            if(isEdit){                    
                if(jph != abs.getjKrHr()){
                    JOptionPane.showMessageDialog(this, "Masukkan ulang jumlah hari hadir!", "Peringatan", JOptionPane.OK_OPTION);
                    //pnlJam.setVisible(true);
                    btHtg.requestFocus();
                    return;
                }else{
                    abs.setIdAbs(Integer.parseInt(txIdAbs.getText()));
                    qab.update(abs, Integer.parseInt(txIdAbs.getText()));
                }
            }else{
                qab.insert(abs);
            }

            JOptionPane.showMessageDialog(this, "Data berhasil disimpan.");
            tampilData();
            statusAwal();         
        }
    }//GEN-LAST:event_btSimpanActionPerformed

