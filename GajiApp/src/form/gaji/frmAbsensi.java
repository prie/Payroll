    private void btTmbhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btTmbhActionPerformed
        // TODO add your handling code here:
        double jk, jm, mt;
        if(tx8Jam.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Jam Kerja perHari harus diisi!", "Peringatan", JOptionPane.OK_OPTION);
            tx8Jam.requestFocus();
            return;
        } else {
            try {
                jk = Double.parseDouble(tx8Jam.getText());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,"Jam Kerja perHari harus diisi Angka!","Peringatan",JOptionPane.OK_OPTION);
                tx8Jam.requestFocus();
                return;
            }
        }
        if(txJam.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Jam harus diisi!", "Peringatan", JOptionPane.OK_OPTION);
            txJam.requestFocus();
            return;
        } else {
            try {
                jm = Double.parseDouble(txJam.getText());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,"Jam harus diisi Angka!","Peringatan",JOptionPane.OK_OPTION);
                txJam.requestFocus();
                return;
            }
        }
        if(txMnt.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Menit harus diisi!", "Peringatan", JOptionPane.OK_OPTION);
            txMnt.requestFocus();
            return;
        } else {
            try {
                mt = Double.parseDouble(txMnt.getText());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,"Menit harus diisi Angka!","Peringatan",JOptionPane.OK_OPTION);
                txMnt.requestFocus();
                return;
            }
        }
        
        /***** Memberi nomor masukan ******/
        lbDt.setText("1");
        int no = Integer.parseInt(lbDt.getText()) ;
        noJam = noJam+no;
        lblNmr.setText(Integer.toString(noJam));

        /***** Menjumlahkan Jam masukkan ******/
        //double jm = Double.parseDouble(txJam.getText()) ;
        jam = jam+jm;

        /***** Menjumlahkan menit masukan ******/
        //double mt = Double.parseDouble(txMnt.getText()) ;
        mnt = mnt+mt;

        /***** Menambahkan menit kedalam jam ******/
        ttljm = jam + Math.ceil(mnt/60);

        /***** Total jam hadir di konvert ke Hari ******/
       // double jk = Double.parseDouble(tx8Jam.getText());
        jth = Math.ceil(ttljm/jk);

        DecimalFormat df = new DecimalFormat("#");
        tx6Hr.setText(df.format(jth));
        txJam.setText("");           
        txMnt.setText("");
        txJam.requestFocus();

        jph = Integer.parseInt(tx8Jam.getText()); //Untuk keperluan syarat edit jam kerja/hari
    }//GEN-LAST:event_btTmbhActionPerformed

