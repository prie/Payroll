    private void btSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSimpanActionPerformed
        // TODO add your handling code here:
        if(evt.getActionCommand().equalsIgnoreCase("Tambah")){
            siapSimpan();
            kosongJText();
            btCrNm.requestFocus();
            isEdit = false;
        }else{
            if(txUsern.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Username harus diisi!", "Peringatan", JOptionPane.OK_OPTION);
                txUsern.requestFocus();
            }else if(pswAdm.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Password harus diisi.!", "Peringatan", JOptionPane.OK_OPTION);
                pswAdm.requestFocus();
            }else if(txIdPeg.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Nama Admin harus dipilih!", "Peringatan", JOptionPane.OK_OPTION);
                btCrNm.requestFocus();
            }
            
            User usr = new User();
            usr.setIdPeg(Integer.parseInt(txIdPeg.getText()));
            usr.setUsrNme(txUsern.getText());
            usr.setPssWrd(pswAdm.getText());
            
            SqlUser qus = new SqlUser(KonekDB.getKonek());
            if(isEdit){
                usr.setIdAdm(Integer.parseInt(txIdAdmn.getText()));
                qus.update(usr, Integer.parseInt(txIdAdmn.getText()));
            }else{
                qus.insert(usr);
            }

            JOptionPane.showMessageDialog(this, "Data berhasil disimpan.");
            tampilData();
            statusAwal();
        }
    }//GEN-LAST:event_btSimpanActionPerformed

