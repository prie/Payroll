    private void tblMAdminMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMAdminMouseClicked
        // TODO add your handling code here:
         tabelKlik();
        int idAdm =Integer.parseInt(tblMAdmin.getValueAt(tblMAdmin.getSelectedRow(),0).toString()) ;
        txIdAdmn.setText(tblMAdmin.getValueAt(tblMAdmin.getSelectedRow(),0).toString());
       // txIdPeg.setText(tblMAdmin.getValueAt(tblMAdmin.getSelectedRow(),1).toString());
        txNmAdm.setText(tblMAdmin.getValueAt(tblMAdmin.getSelectedRow(),1).toString());
        txUsern.setText(tblMAdmin.getValueAt(tblMAdmin.getSelectedRow(),2).toString());
        //pswAdm.setText(tblMAdmin.getValueAt(tblMAdmin.getSelectedRow(),5).toString());
        
        /*-------------------------------------------------------------------*/
        String sql = "SELECT * FROM tbl_user WHERE idUser='"+idAdm+"'";        
        try {
            Connection conn = KonekDB.getKonek();
            Statement sm = conn.createStatement();
            ResultSet rs = sm.executeQuery(sql);
            if(rs.next()){
                txIdPeg.setText(Integer.toString(rs.getInt("idPegawai")));
            }
        } catch (SQLException ex) {
            Logger.getLogger(frmUser.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tblMAdminMouseClicked

