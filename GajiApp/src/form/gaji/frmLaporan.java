    private void btCtkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCtkActionPerformed
        // TODO add your handling code here:
        if(rbRkp.isSelected()){
            String bln = (String) cbBln.getSelectedItem();
            int thn = Integer.parseInt(cbTahun.getSelectedItem().toString());

            reportRekapAll rra = new reportRekapAll(bln, thn);           
            rra.setVisible(true);
        }else if(rbSlpA.isSelected()){
            String bln = (String) cbBln.getSelectedItem();
            int thn = Integer.parseInt(cbTahun.getSelectedItem().toString());

            reportSlipAll rsa = new reportSlipAll(bln, thn, "");
            rsa.setVisible(true);
        }else{
            String slp = (String) cbSlp.getSelectedItem();

            reportSlipAll rsa = new reportSlipAll("", 0, slp);
            rsa.setVisible(true);
        }
    }//GEN-LAST:event_btCtkActionPerformed

