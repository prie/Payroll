    private void isiCombo(){
        cbTahun.removeAllItems();
        int th = 2010;
        for(int i=0;i<11;i++){
            cbTahun.addItem(th);
            th ++;
        }
        cbTahun.setSelectedItem(2013);

        cbSlp.removeAllItems();
        String sql = "SELECT noSlip FROM gaji_rekap";
        Connection conn = KonekDB.getKonek();
        Statement sm;
        try {
            sm = conn.createStatement();
            ResultSet rs = sm.executeQuery(sql);
            while(rs.next()){
                cbSlp.addItem(rs.getString("noSlip"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(frmLaporan.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

